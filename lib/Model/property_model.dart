class PropertyModel {
  final String title;
  final String price;
  final String image;
  final String type;
  final String category;
  final String location;
  final String area;
  final String bedInfo;
  final String contactName;

  PropertyModel({
    required this.title,
    required this.price,
    required this.image,
    required this.type,
    required this.category,
    required this.location,
    required this.area,
    required this.bedInfo,
    required this.contactName
  });
}
